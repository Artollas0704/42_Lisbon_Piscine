#include "bsq.h"

void	ft_atoi(t_bsq *va)
{
	int	i;

	i = 0;
	while ((va->map[i] >= '0' && va->map[i] <= '9') && i < (va->first_line - 4))
	{
		va->n_lines = va->n_lines * 10 + va->map[i] - 48;
		i++;
	}
}
